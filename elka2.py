import turtle

t = turtle.Pen()

t.forward(20)		# рисует вперед на 20 пикселей
t.left(10)			# поворачивает влево на 10 градусов
t.right(10)			# поворачивает направо на 10 пикселей
'''
t.backward(10)		# рисует назад на 20 пикселей
'''
t.color('yellow', 'yellow')
t.begin_fill()
t.end_fill()

turtle.done()  
